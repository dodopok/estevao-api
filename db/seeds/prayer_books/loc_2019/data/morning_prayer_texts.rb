general_thanksgiving = <<~TEXT
  **Deus Todo-Poderoso, Pai de toda a misericórdia,
    nós, teus servos indignos, te damos humildes graças
    por toda a tua bondade e amorosa benevolência
    para conosco e para com todos os que criaste.
  Nós te bendizemos por nossa criação, preservação,
    e por todas as bênçãos desta vida;
    mas, acima de tudo, por teu amor incomensurável
    na redenção do mundo por nosso Senhor Jesus Cristo;
    pelos meios da graça e pela esperança da glória.
  E, te rogamos, dá-nos tal consciência de tuas misericórdias,
    que com corações verdadeiramente agradecidos
    possamos manifestar o teu louvor,
    não apenas com nossos lábios, mas em nossas vidas,
    entregando-nos ao teu serviço
    e caminhando diante de ti
    em santidade e justiça todos os nossos dias;
  Por Jesus Cristo, nosso Senhor,
    a quem, contigo e com o Espírito Santo,
    seja dada honra e glória por todos os séculos. Amém.**
TEXT

